def main():
    print("range(5) :", range(5))
    for variable in range(5):
        print( variable)
        # 0 1 2 3 4

    print("range(1,5) :", range(1,5))
    for variable in range(1,5):
        print( variable)
        # 1 2 3 4
    
    print("range(1,10,2) :", range(1,10,2))
    for variable in range(1,10,2):
        print(variable)
        # 1 3 5 7 9

main()